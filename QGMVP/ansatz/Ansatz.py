def combCircQAOA(
    n: int,
    name: str,
    simulator: Aer,
    p: int,
    initCirc: QuantumCircuit,
    costCirc: QuantumCircuit,
    mixerCirc: QuantumCircuit,
    measure: bool = True,
) -> QuantumCircuit:
    """
    Construct the combined alternating circuit with Inital state, Cost Oprator, Mixer,
    and you also need to give a name to your circuit

    Args:
        n (int): the number of qubits, we will use it to generate classical circuit for
                measurement
        name (str): tell us what name you want to give your combined circuit
        simulator (Aer): give information of simulator
        p (int): the layer of circuit
        initCirc (QuantumCircuit): Initial state generator
        costCirc (QuantumCircuit): Cost Operator circuit
        mixerCirc (QuantumCircuit): Mixer

    Returns: the combined alternating circuit.
    """
    [initCirc, costCirc, mixerCirc], ancillas_num = ancilla_append(
        [initCirc, costCirc, mixerCirc]
    )
    circ = QAOAAnsatz(
        cost_operator=costCirc,
        reps=p,
        initial_state=initCirc,
        mixer_operator=mixerCirc,
        name=name,
    )  # the purpose of use this function, because this will alter the circuit parameter as gamma,beta dictionary

    if measure:
        # exclude the ancilla qubits, but always put ancilla qubits at the end of the circuit
        main_qubit = circ.num_qubits - ancillas_num
        cr = ClassicalRegister(n, "c")
        circ.add_register(cr)
        circ.measure(circ.qubits[0:main_qubit][::-1], cr)
        # [::-1] Change the order of qubits to match the order of the classical register

    circ = transpile(circ, simulator, optimization_level=0)

    return circ


def combGen(n: int, name: str, simulator: Aer, **circs):
    """
    combine general circuit
    p means how many circuit has been added
    """
    qn = QuantumRegister(n, "q")
    circ = QuantumCircuit(qn)
    circ.name = name
    for circsNo in circs:
        circ.append(circs[circsNo], qn[:])

    cr = ClassicalRegister(n, "c")
    circ.add_register(cr)
    circ.measure(circ.qubits[::-1], cr)
    # circ = transpile(circ, simulator, optimization_level=0)

    return circ


def bindParamGene(parameters: list, circ: QuantumCircuit, **kwargs) -> dict:
    """
    Convert a list of parameter to a dictionary, to show the relationship between variable parameters
    and their values.

    parameters: The input parameters.

    Return: The dictionary of parameters

    """

    param = {}
    for i, c in enumerate(circ.parameters):
        param[c] = float(parameters[i])

    return param


def simulate(
    circ: QuantumCircuit,
    simulator: Aer,
    parameters: Union[list, dict],
    numIter: int,
    **kwargs
) -> dict:
    """
    Run quantum circuit generated by combCirc <numIter> and obtain the vector Psi(gamma, beta)

    parameters: Parameters of the quantum circuit, gamma (parameters of U_C), beta (parameters of U_B).

    numIter: The number of iterations.
    and the function also receive any other
    Return: All <numIter> measurement result of the state vector.


    """
    if not isinstance(
        parameters, dict
    ):  # if you drop some list parameters, from initParamGen direcly
        parameters = bindParamGene(parameters=parameters, circ=circ)

    paraCirc = circ.assign_parameters(parameters)
    # paraCirc = transpile(paraCirc, simulator, optimization_level=0)
    # With different backend the circuit must be transpiled to corresonding gate else the back end could not run it properly
    # qObj = assemble(paraCirc, shots=numIter)  # a standard way to generate an experiment
    results = simulator.run(paraCirc, shots=numIter).result()
    rc = results.get_counts()

    return rc


def ancilla_append(circuits: List):
    """Ensure that circuits have the same number of qubits by adding ancilla qubits to the circuit with fewer qubits.

    Args:
        circuits (List): List of circuits to be compared.

    Returns:
        circuits (List): The updated list of circuits with the same number of qubits.
        max_ancillas (int): the number of ancilla qubits
    """
    # Determine the number of qubits in each circuit

    max_ancillas = 0
    max_name = ""
    for i, circ in enumerate(circuits):
        num_ancillas = circ.num_ancillas
        if num_ancillas > max_ancillas:
            max_ancillas = num_ancillas
            for reg in circ.qregs + circ.cregs:
                if isinstance(reg, AncillaRegister):
                    max_name = reg.name
                    break

    # Determine the difference in the number of qubits
    # Iterate through each circuit and add ancilla qubits if necessary
    for circ in circuits:
        num_ancillas = circ.num_ancillas
        if num_ancillas < max_ancillas:
            # Find the ancilla register in the circuit
            for reg in circ.qregs + circ.cregs:
                if isinstance(reg, AncillaRegister):
                    ancilla_register_name = reg.name
                    # Add the required number of ancilla qubits if existing ancilla register is not large enough
                else:
                    ancilla_register_name = max_name
                    # If no ancilla register exists, create a new one with the name of the maximum number of ancilla qubits

            circ.add_register(
                AncillaRegister(max_ancillas - num_ancillas, ancilla_register_name)
            )

    return circuits, max_ancillas
